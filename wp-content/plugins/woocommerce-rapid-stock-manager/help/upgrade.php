<h1>How to upgrade Rapid Stock Manager</h1>
<p>
    Currently the simplest way how you can upgrade to newer version is to replace current woocommerce-rapid-stock-manager 
    directory within the plugins folder.
</p>
<pre>
Your Wordpress directory root
├── wp-content
│   ├── plugins
│   │   │...
│   │   ├── woocommerce
│   │   ├── woocommerce-rapid-stock-manager
│   │   └── woocommerce_uploads
...
</pre>
<p>
    You don't need to be worried about your stock data, as all amounts and data are stored in database, so replacing 
    of your older plugin version does not have any effect on your stock.
</p>
<p>
    We are looking to add more further functionality to allow automatic upgrade in future versions.
</p>

