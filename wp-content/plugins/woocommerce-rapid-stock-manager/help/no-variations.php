<h1>Not products available to list. Please select different variation set. #2</h1>
<h2>Attributes sets</h2>
<p>
    When you see this message, it means that for the current variation set you have selected 
in the dropdown, you don't have any products using it. <br>
    <img src="<?php echo substr(__DIR__,strpos(__DIR__,'/wp-content/')); ?>/images/select_variation_set.png" height="130px">
